# Load data
df_prod = pd.read_csv('data/cleaned/production_cultures.csv')
df_fert = pd.read_csv('data/cleaned/fertilizers_nutrient.csv')
df_temp = pd.read_csv('data/cleaned/mean_temperature.csv')
df_precip = pd.read_csv('data/cleaned/precipitations.csv')
centroids = pd.read_csv('data/cleaned/country_centroids.csv')

# Clean and apply mapping
df_prod['Pays'] = df_prod['Pays'].map(french_to_english).fillna(df_prod['Pays'])
df_fert['Pays'] = df_fert['Pays'].map(french_to_english).fillna(df_fert['Pays'])
df_temp['Pays'] = df_temp['Pays'].map(french_to_english).fillna(df_temp['Pays'])
df_precip['Pays'] = df_precip['Pays'].map(french_to_english).fillna(df_precip['Pays'])

df_rend_agg = df_prod[df_prod['Element'] == 'Rendement'].copy()
df_rend_agg = df_rend_agg[df_rend_agg['Valeur'] <= 100000]
df_rend_agg = df_rend_agg.groupby(['Pays', 'Annee'])['Valeur'].mean().reset_index().rename(columns={'Valeur': 'Rendement'})

df_temp_agg   = df_temp.groupby(['Pays', 'Annee'])['Valeur'].mean().reset_index().rename(columns={'Valeur': 'Temperature_C'})
df_precip_agg = df_precip.groupby(['Pays', 'Annee'])['Valeur'].mean().reset_index().rename(columns={'Valeur': 'Precipitations_mm'})
df_fert_agg = df_fert.groupby(['Pays', 'Annee'])['Valeur'].sum().reset_index().rename(columns={'Valeur': 'Engrais_kgha'})

df_full = df_rend_agg.copy()
df_full = pd.merge(df_full, df_temp_agg, on=['Pays', 'Annee'], how='left')
df_full = pd.merge(df_full, df_precip_agg, on=['Pays', 'Annee'], how='left')
df_full = pd.merge(df_full, df_fert_agg, on=['Pays', 'Annee'], how='left')

# Drop missing targets and key inputs
recents_full = df_full[df_full['Annee'] >= 2010].dropna(subset=['Rendement', 'Engrais_kgha'])

pays_stats = recents_full.groupby('Pays').agg(
    Rendement_med   = ('Rendement',        'median'),
    Engrais_moy     = ('Engrais_kgha',     'mean')
).dropna()

print(f"Total modeling countries (pays_stats): {len(pays_stats)}")

# Compare pays_stats index with centroids COUNTRY
centroid_countries = set(centroids['COUNTRY'].unique())
missing = set(pays_stats.index) - centroid_countries

print(f"\nMissing countries from modeling index ({len(missing)}):")
print(sorted(list(missing)))
